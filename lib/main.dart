import 'dart:ui';

import 'package:cinema_pos/const.dart';
import 'package:cinema_pos/screens/home_screen.dart';
import 'package:flutter/material.dart';

void main() {
  runApp(const MyApp());
}

class MyApp extends StatelessWidget {
